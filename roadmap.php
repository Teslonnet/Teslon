<div class="page-con-container">
<div class="page-in-container">
<div class="markdown darkmode-block">

<p>The Teslon roadmap is a plan for developing and achieving the goals of the Teslon project. It defines the main milestones and tasks that Teslon intends to accomplish in the near future.</p>
<h2><a id="user-content-short-term-goals" class="anchor" aria-hidden="true" href="#short-term-goals"><svg class="octicon octicon-link" viewBox="0 0 16 16" version="1.1" width="16" height="16" aria-hidden="true"><path d="m7.775 3.275 1.25-1.25a3.5 3.5 0 1 1 4.95 4.95l-2.5 2.5a3.5 3.5 0 0 1-4.95 0 .751.751 0 0 1 .018-1.042.751.751 0 0 1 1.042-.018 1.998 1.998 0 0 0 2.83 0l2.5-2.5a2.002 2.002 0 0 0-2.83-2.83l-1.25 1.25a.751.751 0 0 1-1.042-.018.751.751 0 0 1-.018-1.042Zm-4.69 9.64a1.998 1.998 0 0 0 2.83 0l1.25-1.25a.751.751 0 0 1 1.042.018.751.751 0 0 1 .018 1.042l-1.25 1.25a3.5 3.5 0 1 1-4.95-4.95l2.5-2.5a3.5 3.5 0 0 1 4.95 0 .751.751 0 0 1-.018 1.042.751.751 0 0 1-1.042.018 1.998 1.998 0 0 0-2.83 0l-2.5 2.5a1.998 1.998 0 0 0 0 2.83Z"></path></svg></a>Short term goals</h2>
<ul>
<li>Team formation and concept development (Q1 2023): In this phase, the Teslon team will gather experts and develop a project strategy. A market analysis and a study of user needs will be carried out.</li>
<li>Blockchain Platform Development (Q2-Q3 2023): Teslon will work on building its own blockchain platform for Teslon. This will ensure the security and transparency of transactions, as well as expand the capabilities of the project.</li>

</ul>
<h2><a id="user-content-medium-term-goals" class="anchor" aria-hidden="true" href="#medium-term-goals"><svg class="octicon octicon-link" viewBox="0 0 16 16" version="1.1" width="16" height="16" aria-hidden="true"><path d="m7.775 3.275 1.25-1.25a3.5 3.5 0 1 1 4.95 4.95l-2.5 2.5a3.5 3.5 0 0 1-4.95 0 .751.751 0 0 1 .018-1.042.751.751 0 0 1 1.042-.018 1.998 1.998 0 0 0 2.83 0l2.5-2.5a2.002 2.002 0 0 0-2.83-2.83l-1.25 1.25a.751.751 0 0 1-1.042-.018.751.751 0 0 1-.018-1.042Zm-4.69 9.64a1.998 1.998 0 0 0 2.83 0l1.25-1.25a.751.751 0 0 1 1.042.018.751.751 0 0 1 .018 1.042l-1.25 1.25a3.5 3.5 0 1 1-4.95-4.95l2.5-2.5a3.5 3.5 0 0 1 4.95 0 .751.751 0 0 1-.018 1.042.751.751 0 0 1-1.042.018 1.998 1.998 0 0 0-2.83 0l-2.5 2.5a1.998 1.998 0 0 0 0 2.83Z"></path></svg></a>Medium term goals</h2>
<ul>
<li>Mainnet launch and coin issuance (Q4 2024): After the development of the blockchain platform is completed, Teslon will launch the Teslon mainnet and issue a coin. This will allow users to start using Teslon to conduct transactions and earn rewards.</li>
<li>Ecosystem Development and Partnerships (2024): During this phase, Teslon will work to develop the Teslon ecosystem by attracting new users and partners. There will be marketing campaigns and partnership agreements to increase the use of Teslon in various industries.</li>


</ul>
<h2><a id="user-content-long-term-goals" class="anchor" aria-hidden="true" href="#long-term-goals"><svg class="octicon octicon-link" viewBox="0 0 16 16" version="1.1" width="16" height="16" aria-hidden="true"><path d="m7.775 3.275 1.25-1.25a3.5 3.5 0 1 1 4.95 4.95l-2.5 2.5a3.5 3.5 0 0 1-4.95 0 .751.751 0 0 1 .018-1.042.751.751 0 0 1 1.042-.018 1.998 1.998 0 0 0 2.83 0l2.5-2.5a2.002 2.002 0 0 0-2.83-2.83l-1.25 1.25a.751.751 0 0 1-1.042-.018.751.751 0 0 1-.018-1.042Zm-4.69 9.64a1.998 1.998 0 0 0 2.83 0l1.25-1.25a.751.751 0 0 1 1.042.018.751.751 0 0 1 .018 1.042l-1.25 1.25a3.5 3.5 0 1 1-4.95-4.95l2.5-2.5a3.5 3.5 0 0 1 4.95 0 .751.751 0 0 1-.018 1.042.751.751 0 0 1-1.042.018 1.998 1.998 0 0 0-2.83 0l-2.5 2.5a1.998 1.998 0 0 0 0 2.83Z"></path></svg></a>Long term goals</h2>
<ul>
<li>Introducing new features and performance improvements (2025): Teslon will continue to develop and implement new features and improvements to improve Teslon performance and usability. Research and development will be carried out in the field of blockchain technologies and cryptocurrencies.</li>
<li>Scaling and Global Presence (2025+): During this phase, Teslon will look to expand Teslon global footprint by partnering with international organizations and expanding its user base. Events and conferences will be held to draw attention to the project.</li>

</ul>

